package day03_comments_escape_sequence;

public class TabAndNextLine {
    public static void main(String[] args) {
        System.out.println("This is not TABBED");
        System.out.println("\tThis is 1 time TABBED");//this os tabbed one time
        System.out.println("    This is not TABBED"); // 4 spaces equal to 1 tab
        System.out.println("\t\tThis is 2 time TABBED");//this os tabbed 2 times
        // The followings are about new line /n
        System.out.println("1) Go to store");
        System.out.println("2) grab milk");
        System.out.println("3) pay cashier");


        System.out.println("1) Go to store\n2) grab milk\n3) pay cashier");

        //        Command+option+L  TO make code organized
        //
        //        to make all code coment  MAC: command + A , Comand / ,to uncoment command + A , Comand /
    }
}
