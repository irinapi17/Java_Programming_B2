package day10_if_statements;

public class IfWithoutBrackets {
    public static void main(String[] args) {

        if(4 > 2) {
            System.out.println("Hello");
        }
// Single if ststement with Brackets --- which has one statement
        //semicolon ends if statement if no brackets
        System.out.println("bye");





        if(4 > 1) {
            System.out.println("Hello");

            //if with 2 statements
            System.out.println("bye");

        }

        if(5 > 1)
            System.out.println("Hello");
        // if statement without brackets with one statement
        //semicolon ends if statement if no brackets
        System.out.println("bye");
    }
}
